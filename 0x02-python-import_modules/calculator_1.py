#!/usr/bin/python3
def add(a, b):
    """My addition functions

    Args:
        a: first integers
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtraction function

    Args:
        a: first integers
        b: second integers

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplication functions

    Args:
        a: first integers
        b: second integers

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """My division functions

    Args:
        a: first integers
        b: second integers

    Returns:
        The return value. a / b
    """
    return int(a / b)
